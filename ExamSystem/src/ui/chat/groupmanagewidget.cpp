void GroupManageWidget::onAcceptRequestClicked()
{
    if (m_selectedRequestId <= 0) return;

    QMessageBox::StandardButton reply = QMessageBox::question(this,
                                                              "Á°ÆËÆ§Êìç‰Ωú", "Á°ÆÂÆöË¶ÅÂêåÊÑèËøô‰∏™Âä†Áæ§Áî≥ËØ∑ÂêóÔºü",
                                                              QMessageBox::Yes | QMessageBox::No);

    if (reply == QMessageBox::Yes) {
        bool success = m_database->processGroupRequest(m_selectedRequestId, true);
        if (success) {
            QMessageBox::information(this, "Êìç‰ΩúÊàêÂäü", "Â∑≤ÂêåÊÑèÂä†Áæ§Áî≥ËØ∑");
            refreshData();
            emit groupRequestProcessed();
        } else {
            QMessageBox::critical(this, "Êìç‰ΩúÂ§±Ë¥•", "Â§ÑÁêÜÁî≥ËØ∑Â§±Ë¥•ÔºåËØ∑ÈáçËØï");
        }
    }
}

