QString GroupManageWidget::findGroupName(int groupId)
{
    for (const QVariantMap &group : m_createdGroups) {
        if (group["group_id"].toInt() == groupId) {
            return group["group_name"].toString();
        }
    }
    return QString("Áæ§ËÅä #%1").arg(groupId);
}
